import pandas as pd

port_data = pd.read_csv(
    'port_locodes.csv',
    sep=';',
    header=None,
    names=['port_name', 'port_code', 'polygon']
)
port_data

port_data.shape[0]==len(set(port_data['port_code']))

import pandas as pd
country_code_df=pd.read_csv('country-codes.csv')
country_code_df

port_data["country_code"] = port_data["port_code"].str[:2]
port_data

port_data.shape[0]==len(set(port_data['country_code']))

port_data=port_data.merge(country_code_df,how='left',left_on='country_code',right_on='CountryCode').rename(columns={'CountryName':'country_name'}).drop('CountryCode',axis=1)
port_data

print(type(port_data['polygon'][0]))
port_data['polygon'][0]
port_data["polygon_wkt"] = port_data["polygon"].apply(lambda s: f"POLYGON(({s}))")
port_data['polygon']=port_data['polygon_wkt']
port_data=port_data.drop('polygon_wkt',axis=1)

port_data = port_data.reset_index(drop=True)
port_data["port_id"] = port_data.index + 1
port_data = port_data[[
    "port_id",
    "port_code",
    "port_name",
    "country_code",
    "country_name",
    "polygon"
]]
port_data

port_data.to_csv(
    "port_data.csv",
    index=False,
    quoting=1  # csv.QUOTE_ALL
)

